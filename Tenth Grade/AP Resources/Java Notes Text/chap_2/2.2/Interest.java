/*
 * This class implements a simple program that will compute the amount
 * of interest that is earned on $17,000 invested at an interest rate of 0.027
 * for one year. The interest and the value of the investment after one year
 * are printed to standard output.
 *
 * This program was copied from http://math.hws.edu/javanotes/c2/s2.html on 
 * 02-05-2015 by Jesse Evers.
 */

public class Interest {
	
	public static void main(String[] args) {
		
		// Declare the variables

		double principle;  // The value of the investment
		double rate;  // The annual interest rate
		double interest;  // Interest earned in one year

		// Do the computations

		principle = 17000;
		rate = 0.027;
		interest = principle * rate;  // Compute the interest

		principle = principle + interest;
			// Compute value of investment after one year, with interest
			// (Note: the new value replaces the old value of principle.)
		
		// Output the results

		System.out.print("The interest earned is $");
		System.out.println(interest);
		System.out.print("The value of the investment after one year is $");
		System.out.println(principle);

	}  // End of main()

}  // End of class Interest
